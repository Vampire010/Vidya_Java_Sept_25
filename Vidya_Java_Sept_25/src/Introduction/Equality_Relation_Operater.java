package Introduction;

public class Equality_Relation_Operater
{

	public static void main(String[] args) 
	{

		int num1 = 100;
		int num2 = 100;
		
		
		if(num1==num2)
		{
			System.out.println("Numbers are Equal");
		}
		else if(num1!=num2)
		{
			System.out.println("Numbers is not Equal");
			
			if(num1>=num2)
			{
				System.out.println("num1 is => num2");

			}
			
			else if(num1<=2)
			{
				System.out.println("num1 is <= num2");

			}
			
			else if(num1>num2)
			{
				System.out.println("num1 is > num2");

			}
			
			else if(num1<num2)
			{
				System.out.println("num1 is < num2");

			}
			
			
		}
		
		
	}

}
